package Opgave3;

public class Vector2 {
    private double x;
    private double y;

    public void setX(double _x) {
        x = _x;
    }
    public void setY(double _y) {
        y = _y;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }

    public Vector2(double _x, double _y) {
        x = _x;
        y = _y;
    }
}
